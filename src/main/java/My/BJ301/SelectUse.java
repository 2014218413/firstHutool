package My.BJ301;

public class SelectUse {
    private String DeliveryOrderBeginDate;
    private String DeliveryOrderEndDate;
    private String TransportCompany;
    private String Supplier;

    public String getDeliveryOrderBeginDate() {
        return DeliveryOrderBeginDate;
    }

    public void setDeliveryOrderBeginDate(String deliveryOrderBeginDate) {
        DeliveryOrderBeginDate = deliveryOrderBeginDate;
    }

    public String getDeliveryOrderEndDate() {
        return DeliveryOrderEndDate;
    }

    public void setDeliveryOrderEndDate(String deliveryOrderEndDate) {
        DeliveryOrderEndDate = deliveryOrderEndDate;
    }

    public String getTransportCompany() {
        return TransportCompany;
    }

    public void setTransportCompany(String transportCompany) {
        TransportCompany = transportCompany;
    }

    public String getSupplier() {
        return Supplier;
    }

    public void setSupplier(String supplier) {
        Supplier = supplier;
    }
}

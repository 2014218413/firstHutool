public class Ren {
    private String name;
    private double d1;
    private double d2;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getD1() {
        return d1;
    }

    public void setD1(double d1) {
        this.d1 = d1;
    }

    public double getD2() {
        return d2;
    }

    public void setD2(double d2) {
        this.d2 = d2;
    }

    public Ren(String name, double d1, double d2) {
        this.name = name;
        this.d1 = d1;
        this.d2 = d2;
    }

}
